public class Figuras_geometricas {
    String nombre;
    int lados;

    public Figuras_geometricas() {}

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public void setLados(int lados) {
        this.lados = lados;
    }

    public String getNombre() {
        return nombre;
    }

    public int getLados() {
        return lados;
    }
}